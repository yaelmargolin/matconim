# ── 2. DELETE ────────────────────────────────────────────────────────────────
DELETE_IDS = {
    1777472225240,  # קורס בישול בשר — פרטי כניסה בלבד, לא מתכון
    1777472223945,  # א• □ והבים לאפה — ללא מצרכים, תוכן חסר
}

changed = 0
for rid, (new_title, new_cat) in FIXES.items():
    r = lookup.get(rid)
    if r:
        old_title = r['title']
        old_cat   = r['category']
        r['title']    = new_title
        r['category'] = new_cat
        print(f'  [{rid}] "{old_title[:45]}" → "{new_title}" ({old_cat}→{new_cat})')
        changed += 1

before = len(recipes)
recipes = [r for r in recipes if r['id'] not in DELETE_IDS]
deleted = before - len(recipes)

print(f'\nתוקנו: {changed} | נמחקו: {deleted}')

with open('aNote_App/recipes-data.json', 'w') as f:
    json.dump(recipes, f, ensure_ascii=False, indent=2)
print('Saved.')
